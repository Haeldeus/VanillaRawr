package db.stats;

/**
 * An Enumeration, that represents all Stats, that are in the Game.
 * @author Haeldeus
 * @version 1.0
 */
public enum Stat {
  /** The value, that represents the Intellect-Stat. */
  Intellect, 
  /** The value, that represents the Spirit-Stat. */
  Spirit,
  /** The value, that represents the Agility-Stat. */
  Agility,
  /** The value, that represents the Strength-Stat. */
  Strength, 
  /** The value, that represents the Stamina-Stat. */
  Stamina, 
  /** The value, that represents the Armor-Stat. */
  Armor, 
  /** The value, that represents the "Defense Rating"-Stat. */
  DefenseRating,
  /** The value, that represents the "Arcane Resist"-Stat. */
  ArcaneResist, 
  /** The value, that represents the "Fire Resist"-Stat. */
  FireResist, 
  /** The value, that represents the "Frost Resist"-Stat. */
  FrostResist, 
  /** The value, that represents the "Holy Resist"-Stat. */
  HolyResist, 
  /** The value, that represents the "Nature Resist"-Stat. */
  NatureResist, 
  /** The value, that represents the "Shadow Resist"-Stat. */
  ShadowResist,
  /** The value, that represents the "Spell Power"-Stat. */
  SpellPower, 
  /** The value, that represents the "Spell Damage"-Stat. */
  SpellDamage, 
  /** The value, that represents the "Arcane Spell Damage"-Stat. */
  ArcaneSpellDamage, 
  /** The value, that represents the "Fire Spell Damage"-Stat. */
  FireSpellDamage, 
  /** The value, that represents the "Frost Spell Damage"-Stat. */
  FrostSpellDamage, 
  /** The value, that represents the "Holy Spell Damage"-Stat. */
  HolySpellDamage, 
  /** The value, that represents the "Nature Spell Damage"-Stat. */
  NatureSpellDamage, 
  /** The value, that represents the "Shadow Spell Damage"-Stat. */
  ShadowSpellDamage, 
  /** The value, that represents the "Attack Power"-Stat. */
  AttackPower,   
  /** The value, that represents the "Ranged Attack Power"-Stat. */
  RangedAttackPower, 
  /** The value, that represents the "Axe Bonus"-Stat. */
  AxeBonus, 
  /** The value, that represents the "Sword Bonus"-Stat. */
  SwordBonus, 
  /** The value, that represents the "Mana per Five Seconds"-Stat. */
  ManaPerFive, 
  /** The value, that represents the Healthpoints-Stat. */
  HP, 
  /** The value, that represents the Manapoints-Stat. */
  Mana, 
  /** The value, that represents the Energypoints-Stat. */
  Energy, 
  /** The value, that represents the Ragepoints-Stat. */
  Rage, 
  /** The value, that represents the Chance to Hit Stat. */
  HitChance
}
